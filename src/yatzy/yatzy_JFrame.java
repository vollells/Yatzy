    /**
     * Metpd som kontrollerar flödet av spelet och flera andra diverse saker
     */
    private void btnRullaTarn(){
        /**
         * Ifall reRoll har olika värden kan antingen rundan öka eller 
         * vilken spelare som är aktiv 
         */
        if(reRoll == 0){ 
            
            if(discard && firstRound){
                
                JOptionPane.showMessageDialog(null, "Please Discard a Field ", "Discard!",
                    JOptionPane.INFORMATION_MESSAGE);
                setTrue();
                discardDice = true;
             
            
            }else{
                firstRound = true;
                prevN = N;

                if (N < numPlayers){
                    N++; 
                }else if (N >= numPlayers){
                    N = 0;
                    gameRound++;
                    lblTurn.setText("Turn " + gameRound);
                }

                if(gameRound >= 16){
                    
                    calcScore();
                    System.out.println(numPlayers);
                    for (int i = 0; i <= numPlayers; i++) {
                        Scores_JFrame.playerName[i] = Player[i].getNamn();
                    }

                    for(int i = 0; i <= numPlayers ; i++){
                        Scores_JFrame.playerScore[i] = Player[i].getFinalScore();
                    }
                    
                    Scores_JFrame.numPlayers = numPlayers;
                    new Scores_JFrame().setVisible(true); 
                    setVisible(false);
                }


                for (int i = 0; i <= 4 ; i++){
                    if(Dice[i].getReroll() == false){
                        Dice[i].setReroll();
                    }
                }
            
                setFalse();
                updateScore();

            
                for (int i= 0; i<=4 ; i++){
                    Dice[i].setVarde();
                
                }
                checkDice();
            
                reRoll = 2;
                lblPlayerName.setText(Player[N].getNamn()+" | 2 Rerolls left");
                
                
            }
            

        /**
         * Ifall reRoll är större än 0 borde den reRolla alla tärningar som har inte har vlats
         */
        }else if(reRoll>0){
            
            for (int i = 0 ; i <= 4; i++){
                if(Dice[i].getReroll()){
                    Dice[i].setVarde();
                }
            }
            
            setFalse();
            checkDice();
            
            reRoll -= 1;
            if(reRoll ==  0){
                
            }
            lblPlayerName.setText( Player[N].getNamn() +" | 0 Rerolls left");
        }        
        if(reRoll == 1){
           
            lblPlayerName.setText( Player[N].getNamn() +" | 1 Rerolls left");
        
        }
        repaint();
        discard = true;
    }
    
    /**
     * Metod som räknar ut hur många poäng en spelare har i slutet av spelet
     */
    private void calcScore(){
        
        
        for (int i = 0; i < numPlayers; i++) {
            newPlayer temp = Player[i + 1];

            while (i >= 0 && Player[i].getFinalScore() > temp.getFinalScore()){
                Player[i + 1] = Player[i];
                i--;
            }

            Player[i + 1] = temp;
        }

    
    }
    
