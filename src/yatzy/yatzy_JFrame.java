    /**
     * Metod som kontrollerar ifall det finns ett eller två Par och ifall det 
     * finns två returnerar den paret med det största värdet
     * @return trueOrFalse värdet för en av tärningarna i paret
     */
     private int checkPar (){
        
        int trueOrFalse = 0; 
        boolean diceV1 = false;
        boolean diceV2 = false;
        int dice1 = 0;
        int dice2 = 0;
        int dice3 = 0;
        
        outerLoop:
        for(int i = 0; i <= 4; i++){
            for(int j = 0; j <= 4; j++){
                if (Dice[i].getVarde() == Dice[j].getVarde() && i != j){
                    dice1 = i;
                    dice2 = j;
                    diceV1 = true;
                    break outerLoop;
                }
            }
        }
        
        outerLoop:
        for(int i = 0; i <= 4; i++){
            for(int j = 0; j <= 4; j++){
                if (Dice[i].getVarde() == Dice[j].getVarde() && i != j && dice1 != i 
                                        && dice1 != j && dice2 != i  && dice2 != j)
                {
                    dice3 = i;
                    diceV2 = true;
                    break outerLoop;
                }
            }
        }
        
        if (diceV1 == true && diceV2 == true){ 
            if (Dice[dice1].getVarde() > Dice[dice3].getVarde()){
                trueOrFalse = Dice[dice1].getVarde();
            }else{
                trueOrFalse = Dice[dice3].getVarde();
            }   
        }else if(diceV1 == false && diceV2 == true){
            trueOrFalse = Dice[dice3].getVarde();
        }else if (diceV1 == true && diceV2 == false){
            trueOrFalse = Dice[dice1].getVarde();
        }
        return trueOrFalse;
    }
    
